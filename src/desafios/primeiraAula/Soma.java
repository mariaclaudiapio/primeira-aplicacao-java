// Crie uma classe "Soma" em Java para imprimir o resultado da operação "10 + 5" no console

package desafios.primeiraAula;

public class Soma
{
    public static void main(String[] args)
    {
        int primeiroNumero = 10;
        int segundoNumero = 5;
        int soma = primeiroNumero + segundoNumero;

        System.out.printf("O resultado da soma é %d.", soma);
    }
}
