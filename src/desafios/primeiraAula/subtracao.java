// Crie uma classe "Subtração" em Java para imprimir o resultado da operação "10 - 5" no console

package desafios.primeiraAula;

public class subtracao
{
    public static void main(String[] args)
    {
        int primeiroNumero = 10;
        int segundoNumero = 5;

        int subtracao = primeiroNumero - segundoNumero;

        System.out.printf("O resultado da subtração é %d.", subtracao);
    }
}
